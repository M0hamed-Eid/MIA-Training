#include <stdio.h>

void printAsteriskGRU() {
    // Print GRU as a string
    printf("GRU\n");

    // Print GRU in an asterisk shape
    printf("*****	****	*   *\n");
    printf("*       *   *   *   *\n");
    printf("*  **   ****    *   *\n");
    printf("*   *   *   *   *   *\n");
    printf("*****   *    *  *****\n");
}

int main() {
    printAsteriskGRU();
    return 0;
}
